    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        SocialNetwork red = new SocialNetwork(this.rede.getText(),this.nick.getText(),this.url.getText(),origin.getUsuario().getId());
        this.fa.anhadirRedSocial(red);
        
        java.util.List<String> netnames = new java.util.ArrayList<String>();
        
        StringsListModel mRed = (StringsListModel) this.lstRedes.getModel();
        java.util.ArrayList<SocialNetwork> redes = fa.consultarRedes(origin.getUsuario().getId());
        for(app.SocialNetwork r : redes){
            netnames.add(r.getId_plataforma() + " en " + r.getPlataforma());
        }
        
        mRed.setElementos(netnames);
        btnGuardar.setEnabled(false);
        
        rede.setEditable(false);
        rede.setText(null);
        nick.setEditable(false);
        nick.setText(null);
        url.setEditable(false);
        url.setText(null);
        this.lstRedes.setSelectedIndex(-1);
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        String a = (String) lstRedes.getSelectedValue();
        String [] partes = a.split("\\s+");
        
        this.fa.eliminarRedSocial(partes[2], partes[0]);
        
        java.util.List<String> netnames = new java.util.ArrayList<String>();
        
        StringsListModel mRed = (StringsListModel) this.lstRedes.getModel();
        java.util.ArrayList<SocialNetwork> redes = fa.consultarRedes(origin.getUsuario().getId());
        for(app.SocialNetwork r : redes){
            netnames.add(r.getId_plataforma() + " en " + r.getPlataforma());
        }
        
        mRed.setElementos(netnames);
        
        rede.setText(null);
        nick.setText(null);
        url.setText(null);
        this.lstRedes.setSelectedIndex(-1);
    }//GEN-LAST:event_btnEliminarActionPerformed

