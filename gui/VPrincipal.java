    private void btnSaldoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaldoActionPerformed
        String to = JOptionPane.showInputDialog(this, "Inserte la cantidad que desee recargar en su cuenta:");
        float saldo = 0;
        try{
            saldo = Float.parseFloat(to);
        }catch(Exception e){
            this.fa.muestraExcepcion("No se ha podido recargar la cantidad indicada");
            return;
        }
        if(saldo < 0){
            this.fa.muestraExcepcion("El saldo a recargar debe ser una cantidad positiva");
            return;
        }
        this.fa.recargarSaldo(saldo);
        String message = "Se han recargado " + saldo + "€ en su cuenta. "
                + "Su saldo actual es de " + this.fa.consultarSaldo(this.fa.getUsuarioActual().getId()) + "€";
        this.fa.enviarEmailAplicacion(this.fa.getUsuarioActual().getEmail(),"Recarga de saldo", message);
        this.fa.muestraExcepcion("Recarga realizada correctamente. Puede comprobarlo en su correo");
        //TODO meter jpane que notifique update sin ser muestra excpecion
        this.actualizarLblSaldo();
    }//GEN-LAST:event_btnSaldoActionPerformed

