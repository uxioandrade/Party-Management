    private void btnSaldo2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaldo2ActionPerformed
        // TODO add your handling code here:
        String to = JOptionPane.showInputDialog(this, "Inserte la cantidad que desee retirar de su cuenta:");
        float saldo = 0;
        try{
            saldo = Float.parseFloat(to);
        }catch(Exception e){
            this.fa.muestraExcepcion("No se ha podido retirar la cantidad indicada");
            return;
        }
        if(saldo - this.fa.consultarSaldo(this.fa.getUsuarioActual().getId())< 0){
            this.fa.recargarSaldo((-1) * saldo);
            String message = "Se han retirado " + saldo + "€ de su cuenta/n"
                    + "Su saldo actual es de " + this.fa.consultarSaldo(this.fa.getUsuarioActual().getId()) + "€";
            this.fa.enviarEmailAplicacion(this.fa.getUsuarioActual().getEmail(), "Retirada de saldo", message);
            this.fa.muestraExcepcion("Retirada realizada correctamente. Puede comprobarlo en su correo");
            //TODO meter jpane que notifique update sin ser muestra excpecion
            this.actualizarLblSaldo();
        }
        else this.fa.muestraExcepcion("No tiene saldo suficiente para retirar esa cantidad");
        
    }//GEN-LAST:event_btnSaldo2ActionPerformed

